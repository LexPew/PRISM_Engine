#pragma once
#include <PRISM/Renderer/Mesh.h>
#include <PRISM/Renderer/Vertex.h>
#include <vector>
#include <glm/vec3.hpp>

class Cube : public Mesh
{
public:
    Cube(float s = 0.5f)   // s = half-size
    {
        float n = -s;
        float p =  s;

        std::vector<Vertex> vertices = {
            // Top Face (white) - y = +s
            {{n,p,n}, {1,1,1,1}, {0,1}, {0,1,0}},
            {{p,p,n}, {1,1,1,1}, {1,1}, {0,1,0}},
            {{n,p,p}, {1,1,1,1}, {0,0}, {0,1,0}},
            {{p,p,p}, {1,1,1,1}, {1,0}, {0,1,0}},

            // Bottom Face (yellow) - y = -s
            {{n,n,n}, {1,1,0,1}, {0,0}, {0,-1,0}},
            {{p,n,n}, {1,1,0,1}, {1,0}, {0,-1,0}},
            {{n,n,p}, {1,1,0,1}, {0,1}, {0,-1,0}},
            {{p,n,p}, {1,1,0,1}, {1,1}, {0,-1,0}},

            // Left Face (blue) - x = -s
            {{n,n,n}, {0,0,1,1}, {1,0}, {-1,0,0}},
            {{n,p,n}, {0,0,1,1}, {1,1}, {-1,0,0}},
            {{n,n,p}, {0,0,1,1}, {0,0}, {-1,0,0}},
            {{n,p,p}, {0,0,1,1}, {0,1}, {-1,0,0}},

            // Right Face (red) - x = +s
            {{p,n,n}, {1,0,0,1}, {0,0}, {1,0,0}},
            {{p,p,n}, {1,0,0,1}, {0,1}, {1,0,0}},
            {{p,n,p}, {1,0,0,1}, {1,0}, {1,0,0}},
            {{p,p,p}, {1,0,0,1}, {1,1}, {1,0,0}},

            // Front Face (green) - z = +s
            {{n,n,p}, {0,1,0,1}, {0,0}, {0,0,1}},
            {{p,n,p}, {0,1,0,1}, {1,0}, {0,0,1}},
            {{n,p,p}, {0,1,0,1}, {0,1}, {0,0,1}},
            {{p,p,p}, {0,1,0,1}, {1,1}, {0,0,1}},

            // Back Face (orange) - z = -s
            {{n,n,n}, {1,0.5f,0,1}, {1,0}, {0,0,-1}},
            {{p,n,n}, {1,0.5f,0,1}, {0,0}, {0,0,-1}},
            {{n,p,n}, {1,0.5f,0,1}, {1,1}, {0,0,-1}},
            {{p,p,n}, {1,0.5f,0,1}, {0,1}, {0,0,-1}}
        };

        std::vector<unsigned int> indices = {
            0, 1, 2,   2, 1, 3,
            4, 5, 6,   6, 5, 7,
            8, 9, 10,  10, 9, 11,
            12, 13, 14, 14, 13, 15,
            16, 17, 18, 18, 17, 19,
            20, 21, 22, 22, 21, 23
        };

        Initialize(vertices, indices);
    }
};
